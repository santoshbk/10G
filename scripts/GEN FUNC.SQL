/* 
GENERAL FUNCTIONS
----------------------
NVL       - NVL(A,B)        - RETURNS B IF A IS NULL ELSE RETURNS A
NVL2      - NVL(A,B,C)      - RETURN C IF A IS NULL ELSE RETURNS B
NULLIF    - NULLIF(A,B)     - IF A==B RETURNS NULL ELSE RETURNS A
COALESCE  - COALESCE(A,B,C) - IF A IS NOT NULL THEN RETURNS A
                                A IS NULL THEN RETURNS B
                                A & B ARE NULL THEN C
*/

SELECT ENAME, SAL, COMM, SAL+COMM FROM EMP;

SELECT NVL(&B, &C) FROM DUAL;
SELECT ENAME, SAL, COMM, NVL((SAL+COMM),SAL) FROM EMP;

SELECT NVL2(&A, &B, &C) FROM DUAL;
SELECT ENAME, MGR, SAL, COMM, NVL2(MGR, SAL, COMM) FROM EMP;

SELECT NULLIF(&A,&B) FROM DUAL;
SELECT ENAME,LENGTH(ENAME), SAL, LENGTH(SAL), NULLIF(LENGTH(ENAME),LENGTH(SAL)) "NULL IF" FROM EMP;

SELECT COALESCE(&A, &B, &C) FROM DUAL;
SELECT ENAME, COMM, MGR, SAL, COALESCE(COMM, MGR, SAL) FROM EMP;
/* DISPAL ENAME, JOB, SAL, BONUS
BONUS IS BASED ON JOB
IF JOB = CLERK BONUS 2.0*SAL  
        SALESMAN BONUS=2.5*SAL
        ANALYST BONUS =2.25*SAL
        OTHERS ONLY SAL */
SELECT
  ENAME,
  JOB,
  SAL,
  (
    CASE
      WHEN JOB='CLERK'
      THEN 2.0*SAL
      WHEN JOB='SALESMAN'
      THEN 2.5*SAL
      WHEN JOB='ANALYST'
      THEN 2.25*SAL
      ELSE SAL
    END) "BONUS"
FROM
  EMP
ORDER BY
  JOB;
  SELECT
  ENAME,
  SAL,
  JOB,
  DECODE(JOB, 'CLERK', 2.0*SAL, 'SALESMAN', 2.5*SAL, 'ANALYST', 2.25*SAL, SAL)
  "BONUS"
FROM
  EMP
ORDER BY
  JOB;



        
