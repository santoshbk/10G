/*
SET OPERATORS RULES
  > NO OF COLUMNS SHOULD BE SAME
  > CORESPONDING COLUMNS SHOULD HAVE SAME DATA TYPES

SET OPERATORS
  - UNION       - SORTS AND REMOVES DUPLICATES
  - UNION ALL   - UNSORTED WITH DUPLICATES
  - INTERSECT   - 
  
*/



-- DISLAY THE JOBS AVAILBLE IN DEPT 10,20
SELECT DISTINCT(JOB) FROM EMP WHERE DEPTNO IN (10,20);

SELECT JOB FROM EMP WHERE DEPTNO=10
UNION 
SELECT JOB FROM EMP WHERE DEPTNO=20;

SELECT JOB FROM EMP WHERE DEPTNO=10
UNION ALL
SELECT JOB FROM EMP WHERE DEPTNO=20;

--
SELECT JOB FROM EMP WHERE DEPTNO=10
INTERSECT
SELECT JOB FROM EMP WHERE DEPTNO=20;

-- FIND JOB UNIQUE TO 1ST QUERY
SELECT JOB FROM EMP WHERE DEPTNO=10
MINUS
SELECT JOB FROM EMP WHERE DEPTNO=20;


